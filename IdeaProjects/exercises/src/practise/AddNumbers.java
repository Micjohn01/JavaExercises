package practise;
//METHOD OVERLOADING

public class AddNumbers {
    public int addition(int num1, int num2) {
        return num1 + num2;
    }
    public double addition(int num1, double num2){
        return num1 + num2;
    }
    public double addition(double num1, double num2){
        return num1 + num2;
    }
    public double addition(double num1, int num2){
        return num1 + num2;
    }

}
